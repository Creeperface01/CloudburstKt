package com.creeperface.cloudburst.kotlin.vector

import com.nukkitx.math.vector.*

//Vector3i
operator fun Vector3i.plus(vec: Vector3i): Vector3i {
    return this.add(vec)
}

operator fun Vector3i.minus(vec: Vector3i): Vector3i {
    return this.sub(vec)
}

operator fun Vector3i.div(vec: Vector3i): Vector3i {
    return this.div(vec)
}

operator fun Vector3i.times(vec: Vector3i): Vector3i {
    return this.mul(vec)
}

operator fun Vector3i.unaryMinus(): Vector3i {
    return this.negate()
}

